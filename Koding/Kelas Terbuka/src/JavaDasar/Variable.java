package JavaDasar;

public class Variable {
    public static void main(String[] args) {
        //Variabel (tipedata , namavar = value)

        int x = 10;
        System.out.println("nilai x = " + x);
        x = 20; // ini assingment dengan tanda (=)
        System.out.println("nilai x baru = " + x);

        //deklarasi =>

        int b; // deklarasi b adalah sebuah integer

        //Assignment
        b = 7; // assigment =>  b adalah bernilai 7


    }
 }
